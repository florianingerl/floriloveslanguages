Vocabulaire utile pour le test:

donner = dar

yo doy, tú das, el da, nosostros damos, vosostros dais, ellos dan

regalar = offrir

yo regalo, tú regalas, el regala, nosostros regalamos, vosostros regalais, ellos ragalan



