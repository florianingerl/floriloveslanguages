Once upon a time, in a small village in the country, there was an old monument of a fish that had once rescued the whole village.

The name of this fish was Karl and he became a hero of the village when on a stormy night he begged the God of the Seas, Poseidon to spare the village from the huge waves that were created in the storm.
That night, there was such a big thunder, lightning and rain, that the whole village gathered together in the house of the chief of the village and everyone was afraid that the sky might fall on their heads. In vain, the chief sent some prayers to the sky, but they were useless.
Although the chief prayed all night with all his heart, there were no answers given to his demands. At least that is how  it appeared for many hours while the storm continued on. However, around 3 am in the morning, there was a bright light coming from the sea and a tiny fish appeared bringing good news.
First good news, by chance or by the will of God, the fish fell into the aquarium of the chief
and thus didn't have to die. It was a golden fish and looking at its golden color gave everyone hope again.
Admiring the golden fish, the chief asked him if the fish could help them survive the storm and save their beloved village.
"But I am only a small fish and you are a big chief with a lot of power! Why the hell are you asking me this? In my fish village, where this sudden storm brought me away from, I was just a servant who cleaned the house of the chief fish."
Even though the chief was shocked to learn that there was a fish community living in the depth of the sea, he turned towards his new guest with hospitality and kindness.
"Judging by your bright , golden colour , I am convinced that you must have extraordinary powers. " spoke the chief. "And as a fish, you are an expert in water-matters and thus please help us to make the storm stop!"
"Such kind words! Thank you so much. But I'm afraid I am not as powerful as you might think. I am just a servant to my great master, the God of Poseidon. Unlike Him, I don't have the capabilities to create or end storms unfortunately." answered the golden fish.
Because the chief interpreted these words as pure modesty and not as truth, he said to the fish: "Please don't make jokes now. You are a water-expert and we really need your help now. There is already water coming through the ceiling. Please help and stop this with your expertise."
Since these were the most desperate words the fish had ever heard, he decided to help the chief and the village.

" I can see that you are as loyal to your people as my great master is to us. So I will try to help you by going and asking for mercy for you from my chief, Poseidon." said the golden fish and before the human chief could have answered, the fish disappeared.
Finding himself now in the deepest sea in the house of Poseidon, the fish asked the latter:
"Sorry to disturb you Poseidon. It's only a small matter. There is a village of people who easily get afraid of small storms and would you help them by teaching them a lesson of bravery? "
"You are lucky that I always liked you Karl and that you have always been a good servant so I am not mad at you for interrupting my storm-creating ceremony. I will do as you asked and help you teach these humans a good lesson of bravery but I need something in exchange from them. Go back to the village and tell them not to ever fish again in the sea!"
Finding himself again in front of the chief, the fish spoke: "You must make a vow that you will never fish again in the sea. If you do that, Poseidon consented to give you bravery, so that you aren't afraid of such small and harmless storms in the future."
Among the people gathered in the chief house on that stormy night was the storyteller of the village. As soon as the storm passed in the morning, this man hurried home and wrote about what happened that night in the book titled 'The History of Our Village'.
In addition, the artist of the village, made a monument in honour of the golden fish and they put it in the center of the village where you can see it up to now.

